#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import ts from "typescript";

const WEBSITE_DIR = path.resolve(new URL("..", import.meta.url).pathname);
const REPO_DIR = path.resolve(WEBSITE_DIR, "..");
const PACKAGE_DIR = path.join(REPO_DIR, "packages", "ts");
const PACKAGE_NAME = "@graphrefly/ts";
const API_DIR = path.join(WEBSITE_DIR, "src", "content", "docs", "api");
const GENERATED_DIR = path.join(WEBSITE_DIR, "src", "generated");
const SIDEBAR_PATH = path.join(GENERATED_DIR, "api-sidebar.mjs");
const GENERATOR_MARKER = "<!-- generated by website/scripts/gen-api-docs.mjs; do not edit -->";
const CHECK = process.argv.includes("--check");
const REPORT_MISSING = process.argv.includes("--report-missing");

const sourceFileCache = new Map();
const exportCache = new Map();
const exportStack = new Set();

function readText(filePath) {
	return fs.readFileSync(filePath, "utf8");
}

function writeIfChanged(filePath, text) {
	if (fs.existsSync(filePath) && readText(filePath) === text) return false;
	fs.mkdirSync(path.dirname(filePath), { recursive: true });
	fs.writeFileSync(filePath, text);
	return true;
}

function parseSourceFile(filePath) {
	const cached = sourceFileCache.get(filePath);
	if (cached) return cached;
	const source = ts.createSourceFile(filePath, readText(filePath), ts.ScriptTarget.Latest, true);
	sourceFileCache.set(filePath, source);
	return source;
}

function packageSubpathForEntry(entry) {
	const withoutSrc = entry.replace(/^src\//, "").replace(/\.ts$/, "");
	if (withoutSrc === "index") return ".";
	if (withoutSrc.endsWith("/index")) return `./${withoutSrc.slice(0, -"/index".length)}`;
	return `./${withoutSrc}`;
}

function importPathForSubpath(subpath) {
	return subpath === "." ? PACKAGE_NAME : `${PACKAGE_NAME}/${subpath.slice(2)}`;
}

function discoverEntries() {
	const tsupConfig = readText(path.join(PACKAGE_DIR, "tsup.config.ts"));
	const packageJson = JSON.parse(readText(path.join(PACKAGE_DIR, "package.json")));
	const exportedSubpaths = new Set(Object.keys(packageJson.exports ?? {}));
	const entries = [...tsupConfig.matchAll(/["'](src\/[^"']+\.ts)["']/g)]
		.map((match) => match[1])
		.map((entry) => ({
			entry,
			filePath: path.join(PACKAGE_DIR, entry),
			subpath: packageSubpathForEntry(entry),
		}))
		.filter((entry) => exportedSubpaths.has(entry.subpath));
	return entries;
}

function hasExportModifier(node) {
	return Boolean(
		node.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword),
	);
}

function cloneDeclaration(info, name) {
	return { ...info, name };
}

function collectDirectExport(statement, sourceFile) {
	if (ts.isFunctionDeclaration(statement) && statement.name && hasExportModifier(statement)) {
		return {
			kind: "function",
			name: statement.name.text,
			node: statement,
			sourceFile,
			filePath: sourceFile.fileName,
		};
	}
	if (ts.isClassDeclaration(statement) && statement.name && hasExportModifier(statement)) {
		return {
			kind: "class",
			name: statement.name.text,
			node: statement,
			sourceFile,
			filePath: sourceFile.fileName,
		};
	}
	return null;
}

function resolveRelativeModule(fromFile, specifier) {
	if (!specifier.startsWith(".")) return null;
	const base = path.resolve(path.dirname(fromFile), specifier);
	const candidates = [];
	if (base.endsWith(".js") || base.endsWith(".mjs") || base.endsWith(".cjs")) {
		candidates.push(base.replace(/\.(mjs|cjs|js)$/, ".ts"));
	} else {
		candidates.push(`${base}.ts`);
	}
	candidates.push(path.join(base, "index.ts"));
	return candidates.find((candidate) => fs.existsSync(candidate)) ?? null;
}

function collectExports(filePath) {
	const normalizedPath = path.normalize(filePath);
	if (exportCache.has(normalizedPath)) return new Map(exportCache.get(normalizedPath));
	if (exportStack.has(normalizedPath)) return new Map();
	exportStack.add(normalizedPath);

	const sourceFile = parseSourceFile(normalizedPath);
	const exports = new Map();
	for (const statement of sourceFile.statements) {
		const direct = collectDirectExport(statement, sourceFile);
		if (direct) exports.set(direct.name, direct);

		if (!ts.isExportDeclaration(statement) || !statement.moduleSpecifier) continue;
		const specifier = statement.moduleSpecifier.text;
		const resolved = resolveRelativeModule(normalizedPath, specifier);
		if (!resolved) continue;
		const exported = collectExports(resolved);

		if (!statement.exportClause) {
			for (const [name, info] of exported) exports.set(name, info);
			continue;
		}
		if (!ts.isNamedExports(statement.exportClause)) continue;
		for (const element of statement.exportClause.elements) {
			const originalName = (element.propertyName ?? element.name).text;
			const exportedInfo = exported.get(originalName);
			if (!exportedInfo) continue;
			exports.set(element.name.text, cloneDeclaration(exportedInfo, element.name.text));
		}
	}

	exportStack.delete(normalizedPath);
	exportCache.set(normalizedPath, new Map(exports));
	return exports;
}

function rawJsDocFor(node, sourceFile) {
	const jsDocs = node.jsDoc;
	if (jsDocs?.length > 0) return jsDocs[jsDocs.length - 1].getFullText(sourceFile);
	const source = sourceFile.getFullText();
	const ranges = ts.getLeadingCommentRanges(source, node.getFullStart()) ?? [];
	const jsDocRange = ranges
		.filter((range) => source.slice(range.pos, range.pos + 3) === "/**")
		.at(-1);
	return jsDocRange ? source.slice(jsDocRange.pos, jsDocRange.end) : "";
}

function cleanJsDocLines(raw) {
	return raw
		.replace(/^\/\*\*/, "")
		.replace(/\*\/$/, "")
		.split(/\r?\n/)
		.map((line) => line.replace(/^\s*\* ?/, "").trimEnd());
}

function parseJsDoc(raw) {
	const lines = cleanJsDocLines(raw);
	const summary = [];
	const tags = [];
	let currentTag = null;
	for (const line of lines) {
		const trimmed = line.trim();
		const tagMatch = trimmed.match(/^@(\S+)\s*(.*)$/);
		if (tagMatch) {
			currentTag = { name: tagMatch[1], value: tagMatch[2] ?? "" };
			tags.push(currentTag);
			continue;
		}
		if (currentTag) {
			currentTag.value += currentTag.value.length === 0 ? line : `\n${line}`;
			continue;
		}
		if (trimmed.length > 0 || summary.length > 0) summary.push(line);
	}
	return {
		summary: summary.join("\n").trim(),
		tags,
	};
}

function tagValues(doc, tagName) {
	return doc.tags.filter((tag) => tag.name === tagName).map((tag) => tag.value.trim());
}

function parseParamDocs(doc) {
	const params = new Map();
	for (const value of tagValues(doc, "param")) {
		const match = value.match(/^([A-Za-z0-9_$]+)(?:\s*-\s*|\s+)([\s\S]*)$/);
		if (!match) continue;
		params.set(match[1], normalizeInline(match[2]));
	}
	return params;
}

function normalizeInline(value) {
	return value
		.split(/\r?\n/)
		.map((line) => line.trim())
		.filter(Boolean)
		.join(" ");
}

function parameterName(parameter) {
	if (ts.isIdentifier(parameter.name)) return parameter.name.text;
	return parameter.name.getText();
}

function parameterType(parameter, sourceFile) {
	return parameter.type?.getText(sourceFile) ?? "unknown";
}

function returnType(node, sourceFile) {
	return node.type?.getText(sourceFile) ?? "unknown";
}

function isApiDocumented(info) {
	const doc = parseJsDoc(rawJsDocFor(info.node, info.sourceFile));
	const missing = [];
	if (!doc.summary) missing.push("summary");
	if (info.kind === "function") {
		if (tagValues(doc, "returns").length === 0) missing.push("@returns");
		const paramDocs = parseParamDocs(doc);
		for (const parameter of info.node.parameters) {
			const name = parameterName(parameter);
			if (!paramDocs.has(name)) missing.push(`@param ${name}`);
		}
	}
	if (tagValues(doc, "category").length === 0) missing.push("@category");
	if (tagValues(doc, "example").length === 0) missing.push("@example");
	return { documented: doc.summary.length > 0, complete: missing.length === 0, doc, missing };
}

function subpathDepth(subpath) {
	return subpath === "." ? 0 : subpath.split("/").length;
}

function discoverDocumentedSymbols() {
	const candidates = [];
	const missingSymbols = [];
	for (const entry of discoverEntries()) {
		for (const info of collectExports(entry.filePath).values()) {
			const result = isApiDocumented(info);
			if (!result.documented) {
				missingSymbols.push({
					name: info.name,
					kind: info.kind,
					subpath: entry.subpath,
					filePath: path.relative(REPO_DIR, info.filePath),
					missing: result.missing,
				});
				continue;
			}
			candidates.push({
				...info,
				doc: result.doc,
				missingDocs: result.missing,
				importPath: importPathForSubpath(entry.subpath),
				subpath: entry.subpath,
			});
		}
	}

	if (REPORT_MISSING) {
		for (const missing of missingSymbols) {
			console.log(
				[
					missing.name,
					missing.kind,
					missing.subpath,
					missing.filePath,
					missing.missing.join(", "),
				].join("\t"),
			);
		}
	}

	const byName = new Map();
	for (const candidate of candidates) {
		const current = byName.get(candidate.name);
		if (!current || subpathDepth(candidate.subpath) > subpathDepth(current.subpath)) {
			byName.set(candidate.name, candidate);
		}
	}
	return [...byName.values()].sort(compareSymbols);
}

function categoryFor(info) {
	const tagged = tagValues(info.doc, "category")[0];
	if (tagged) return tagged;
	if (info.subpath !== ".") return info.subpath.slice(2).split("/")[0];
	const sourcePath = path.relative(path.join(PACKAGE_DIR, "src"), info.filePath);
	return sourcePath.split(path.sep)[0] ?? "other";
}

function compareSymbols(a, b) {
	return (
		categoryFor(a).localeCompare(categoryFor(b)) ||
		a.filePath.localeCompare(b.filePath) ||
		a.node.pos - b.node.pos ||
		a.name.localeCompare(b.name)
	);
}

function frontmatterString(value) {
	return JSON.stringify(value.replace(/\s+/g, " ").trim());
}

function markdownEscapeTable(value) {
	return value.replaceAll("|", "\\|").replaceAll("\n", " ");
}

function signatureFor(info) {
	if (info.kind === "class") return `class ${info.name}`;
	const params = info.node.parameters.map((parameter) => parameter.getText(info.sourceFile));
	if (params.length <= 1) {
		return `function ${info.name}(${params.join(", ")}): ${returnType(info.node, info.sourceFile)}`;
	}
	const joined = params.map((param) => `\t${param}`).join(",\n");
	return `function ${info.name}(\n${joined},\n): ${returnType(info.node, info.sourceFile)}`;
}

function renderApiPage(info) {
	const title = info.kind === "function" ? `${info.name}()` : info.name;
	const description = normalizeInline(info.doc.summary);
	const paramDocs = parseParamDocs(info.doc);
	const returns = tagValues(info.doc, "returns").map(normalizeInline);
	const examples = tagValues(info.doc, "example").map((example) => example.trim());
	const remarks = tagValues(info.doc, "remarks").map(normalizeInline);
	const sourcePath = path.relative(REPO_DIR, info.filePath);
	const lines = [
		"---",
		`title: ${frontmatterString(title)}`,
		`description: ${frontmatterString(description)}`,
		"---",
		"",
		GENERATOR_MARKER,
		"",
		info.doc.summary.trim(),
		"",
		"## Import",
		"",
		"```ts",
		`import { ${info.name} } from "${info.importPath}";`,
		"```",
		"",
		"## Signature",
		"",
		"```ts",
		signatureFor(info),
		"```",
		"",
	];

	if (info.kind === "function" && info.node.parameters.length > 0) {
		lines.push("## Parameters", "", "| Parameter | Type | Description |", "|---|---|---|");
		for (const parameter of info.node.parameters) {
			const name = parameterName(parameter);
			const description = paramDocs.get(name) ?? `Missing @param ${name} JSDoc.`;
			lines.push(
				`| \`${name}\` | \`${markdownEscapeTable(parameterType(parameter, info.sourceFile))}\` | ${markdownEscapeTable(description)} |`,
			);
		}
		lines.push("");
	}

	if (returns.length > 0) {
		lines.push("## Returns", "", returns.join("\n\n"), "");
	} else if (info.kind === "function") {
		lines.push("## Returns", "", `\`${returnType(info.node, info.sourceFile)}\``, "");
	}

	for (let index = 0; index < examples.length; index += 1) {
		lines.push(index === 0 ? "## Example" : `## Example ${index + 1}`, "", examples[index], "");
	}

	if (remarks.length > 0) {
		lines.push("## Remarks", "");
		for (const remark of remarks) lines.push(`- ${remark}`);
		lines.push("");
	}

	if (info.missingDocs.length > 0) {
		lines.push(
			"## Documentation Status",
			"",
			"This page was generated from source JSDoc, but the structured API tags are incomplete:",
			"",
		);
		for (const missing of info.missingDocs) lines.push(`- Missing \`${missing}\`.`);
		lines.push("");
	}

	lines.push("## Source", "", `\`${sourcePath}\``, "");
	return `${lines.join("\n").replace(/\n{3,}/g, "\n\n")}\n`;
}

function slugFor(info) {
	return info.slug ?? info.name.toLowerCase();
}

function fileNameFor(info) {
	return `${slugFor(info)}.md`;
}

function assignSlugs(symbols) {
	const baseCounts = new Map();
	for (const info of symbols) {
		const base = info.name.toLowerCase();
		baseCounts.set(base, (baseCounts.get(base) ?? 0) + 1);
	}
	const used = new Set();
	for (const info of symbols) {
		const base = info.name.toLowerCase();
		let slug =
			baseCounts.get(base) === 1 || (info.kind === "function" && !used.has(base))
				? base
				: `${base}-${info.kind}`;
		let suffix = 2;
		while (used.has(slug)) {
			slug = `${base}-${info.kind}-${suffix}`;
			suffix += 1;
		}
		used.add(slug);
		info.slug = slug;
	}
}

function sidebarLabel(info) {
	return info.kind === "function" ? `${info.name}()` : info.name;
}

const CATEGORY_ORDER = [
	"core",
	"graph",
	"operators",
	"sources",
	"data",
	"data-structures",
	"storage",
	"render",
	"adapters",
	"composition",
	"messaging",
	"cqrs",
	"orchestration",
	"patterns",
	"solutions",
	"testing",
	"work-queue",
	"inspection",
	"executors",
	"node",
	"batch",
	"dispatcher",
];

function categorySortKey(category) {
	const index = CATEGORY_ORDER.indexOf(category);
	return index === -1 ? CATEGORY_ORDER.length : index;
}

function categoryLabel(category) {
	return category
		.split("-")
		.map((part) => part[0].toUpperCase() + part.slice(1))
		.join(" ");
}

function renderSidebar(symbols) {
	const groups = new Map();
	for (const info of symbols) {
		const category = categoryFor(info);
		const entries = groups.get(category) ?? [];
		entries.push({ label: sidebarLabel(info), link: `/api/${slugFor(info)}` });
		groups.set(category, entries);
	}
	const sidebar = [...groups.entries()]
		.sort(
			([a], [b]) =>
				categorySortKey(a) - categorySortKey(b) || categoryLabel(a).localeCompare(categoryLabel(b)),
		)
		.map(([category, items]) => ({
			label: categoryLabel(category),
			collapsed: true,
			items,
		}));
	return `// ${GENERATOR_MARKER.replace("<!-- ", "").replace(" -->", "")}\nexport const apiSidebar = ${JSON.stringify(sidebar, null, "\t")};\n`;
}

function generatedApiFiles() {
	if (!fs.existsSync(API_DIR)) return [];
	return fs
		.readdirSync(API_DIR)
		.filter((name) => name.endsWith(".md"))
		.map((name) => path.join(API_DIR, name))
		.filter((filePath) => readText(filePath).includes(GENERATOR_MARKER));
}

const symbols = discoverDocumentedSymbols();
assignSlugs(symbols);
const outputs = new Map();
for (const symbol of symbols) {
	outputs.set(path.normalize(path.join(API_DIR, fileNameFor(symbol))), renderApiPage(symbol));
}
outputs.set(path.normalize(SIDEBAR_PATH), renderSidebar(symbols));
const outputKeysCaseFolded = new Set([...outputs.keys()].map((filePath) => filePath.toLowerCase()));

let changed = false;
for (const [filePath, expected] of outputs) {
	const actual = fs.existsSync(filePath) ? readText(filePath) : "";
	if (actual === expected) continue;
	changed = true;
	if (CHECK) {
		console.error(`stale generated docs: ${path.relative(REPO_DIR, filePath)}`);
	} else {
		writeIfChanged(filePath, expected);
	}
}

for (const filePath of generatedApiFiles()) {
	const normalized = path.normalize(filePath);
	if (outputs.has(normalized) || outputKeysCaseFolded.has(normalized.toLowerCase())) continue;
	changed = true;
	if (CHECK) {
		console.error(`stale generated docs: ${path.relative(REPO_DIR, filePath)}`);
	} else {
		fs.unlinkSync(filePath);
	}
}

if (CHECK && changed) {
	process.exitCode = 1;
} else {
	console.log(`generated ${symbols.length} TS API page${symbols.length === 1 ? "" : "s"}`);
}
